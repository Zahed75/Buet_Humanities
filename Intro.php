                    <h2>Introduction</h2>
                    <p>The idea of two cultures — one belonging to Humanities and the other to Science and Technology — as being separated from, and antagonistic to, each other is both untenable and harmful. The Humanities Department of BUET is based on the very real awareness that students of Engineering and Technology need to study Humanities to widen their sympathies and perspectives as well as to equip themselves adequately to relate theory to practice and practice to theory. The Department provides the students of BUET with proper knowledge in the fields of Accounting, English, Economics, Management, Political Science, Philosophy, Psychology, Sociology and the like, belonging to the Faculties of Arts, Social Science, and Commerce. Drawing upon recent thinking in the creative world each subject considers theoretical exposure, examines existing realities, includes essential elements, and offers a concise but indispensable proportion of works in each field to acquaint the students with the differences and diversity in the world of learning. In order to improve students’ skills in English Language two Language Laboratories furnished with modern technical equipment have been set up in the Humanities Department. The Department is playing its required role in producing competent engineers, architects and planners with capacity to prove themselves successful and efficient not only within the country but also abroad. As a multidisciplinary Department it aims at developing in the students such faculties as would help them orient themselves socially and intellectually in the present-day predominantly technological world. It is said that science and technology have given human beings motion but have robbed them of emotion without which the human mind is a mere useless vacuity. In whatever area an individual may work for a living, he/she cannot function without regular reciprocal social interactions. For this purpose, the individual needs a behavioral aptitude which science and technology alone cannot provide him/her with. Thus the Humanities Department aims at equipping the future engineers, architects and planners with meaningful knowledge to deal with situations in their professional and day-to-day life.</p>
